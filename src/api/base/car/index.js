import request from '@/utils/request'

/**
 * 获取轮播图列表
 * @param {参数} param
 */
export function getCarList(param) {
  return request.fetchPost('/v1/car/selectList', param)
}

/**
 * 获取轮播图详情
 * @param {参数} param
 */
export function getCar(param) {
  return request.fetchPost('/v1/car/select', param)
}

/**
 * 添加轮播图
 * @param {参数} param
 */
export function setCar(param) {
  return request.fetchPost('/v1/car/insert', param)
}

/**
 * 修改轮播图
 * @param {参数} param
 */
export function updCar(param) {
  return request.fetchPost('/v1/car/update', param)
}

/**
 * 修改轮播图
 * @param {参数} param
 */
export function delCar(param) {
  return request.fetchPost('/v1/car/delete', param)
}

