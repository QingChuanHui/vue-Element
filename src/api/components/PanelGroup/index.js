import request from '@/utils/request'

/**
 * 获取今日统计
 * @param {参数} param
 */
export function getDayReport(param) {
  return request.fetchPost('/v1/index/getDayReport', param)
}

/**
 * 获取维修厂今日统计
 * @param {参数} param
 */
export function getClientDayReport(param) {
  return request.fetchPost('/v1/index/getDayReportToClient', param)
}
